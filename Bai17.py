a = int(input("Số nguyên thứ nhất:"))
b = int(input("Số nguyên thứ hai:"))
c = int(input("Số nguyên thứ ba:"))
A = max(a,b,c)
B = min(a,b,c)
print("Số nguyên lớn nhất là:",A)
print("Số nguyên nhỏ nhất là:",B)